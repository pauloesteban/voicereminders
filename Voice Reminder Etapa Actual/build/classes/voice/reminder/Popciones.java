    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Ppagina1 formu=new Ppagina1();
        Pnuevo fo=new Pnuevo();
        
        String colo;
        
        colo=jComboBox1.getSelectedItem().toString();
        
        if(colo=="Azul"){
            formu.getContentPane().setBackground(Color.BLUE);
            fo.getContentPane().setBackground(Color.BLUE);
            formu.setVisible(true);
            fo.setVisible(true);//por que a 1 le pones falso y el otro no
            this.setVisible(false);
        }else{
            if(colo=="Amarillo"){
                formu.getContentPane().setBackground(Color.yellow);
                fo.getContentPane().setBackground(Color.YELLOW);
                formu.setVisible(true);
                this.setVisible(false);
            }else
                if(colo=="Naranja"){
                    formu.getContentPane().setBackground(Color.ORANGE);
                    fo.getContentPane().setBackground(Color.ORANGE);
                    formu.setVisible(true);
                    this.setVisible(false);
                }else{
                    if(colo=="Rojo"){
                        formu.getContentPane().setBackground(Color.RED);
                        fo.getContentPane().setBackground(Color.RED);
                        formu.setVisible(true);
                        this.setVisible(false);
                    }else{
                        if(colo=="Verde"){
                            formu.getContentPane().setBackground(Color.GREEN);
                            fo.getContentPane().setBackground(Color.GREEN);
                            formu.setVisible(true);
                            this.setVisible(false);
                        }else{
                            formu.getContentPane().setBackground(Color.cyan);
                            fo.getContentPane().setBackground(Color.cyan);
                            formu.setVisible(true);
                            this.setVisible(false);
                        }
                    }
                }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

